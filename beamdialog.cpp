#include "beamdialog.h"

BeamDialog::BeamDialog(Beam &a_beam,QWidget *parent) :
    QDialog(parent),
    beam(a_beam)
{
    setupUi(this);
#if defined(Q_WS_S60)
    showMaximized();
#endif
    spanLength->setValue(beam.length());
}

void BeamDialog::changeEvent(QEvent *e)
{
    QDialog::changeEvent(e);
    switch (e->type()) {
    case QEvent::LanguageChange:
        retranslateUi(this);
        break;
    default:
        break;
    }
}
